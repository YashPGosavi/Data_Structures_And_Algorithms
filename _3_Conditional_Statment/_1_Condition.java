/*
* Conditional Statments.
 1) >   --> Greater than.
 2) <   --> Smaller than.
 3) >=  --> Greater than Equels to.
 4) <=  --> Smaller than Equels to
 5) ==  --> Equels to.
 6) !=  --> Not Equeles to.
 */

public class _1_Condition{
    public static void main(String[] args) {
        boolean var = (9>10);
        System.out.println("9>10"+var);

        var = (9<10);
        System.out.println("9<10"+var);

         var = (9>=10);
        System.out.println("9>=10"+var);

        var = (9<=10);
        System.out.println("9<=10"+var);

         var = (9==10);
        System.out.println("9==10"+var);

        var = (9!=10);
        System.out.println("9!=10"+var);
    }
}